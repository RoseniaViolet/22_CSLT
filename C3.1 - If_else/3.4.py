toan= float(input())
ly= float(input())
hoa= float(input())
DTB= (toan*2 + ly*3 + hoa)/6
if DTB < 3: 
    print('Kem')
if 3<= DTB < 5:
    print('Yeu')
if 5<= DTB < 6:
    print('Trung binh')
if 6<= DTB < 7:
    print('Trung binh kha')
if 7<= DTB < 8:
    print('Kha')
if 8<= DTB < 9:
    print('Gioi')
if 9<= DTB < 10:
    print('Xuat sac')