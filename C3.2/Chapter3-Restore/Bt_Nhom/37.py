n=int(input('Enter the number of sides of the shape:'))
if n==3:
    print('Triangle')
elif n==4:
    print('Tetragon')
elif n==5:
    print('Pentagon')
elif n==6:
    print('Hexagon')
elif n==7:
    print('Heptagon')
elif n==8:
    print('Octagon')
elif n==9:
    print('Nonagon')
elif n==10:
    print('Decagon')
else:
    print('invalid edge number')