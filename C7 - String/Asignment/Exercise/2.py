# a. 'Hello'.upper()
# Phương thức .upper() sẽ chuyển đổi tất cả các ký tự trong chuỗi thành chữ hoa. Vì vậy, 'Hello'.upper() sẽ trả về 'HELLO'.

# b. 'Hello'.upper().isupper()
# Phương thức .isupper() kiểm tra xem tất cả các ký tự trong chuỗi có phải là chữ hoa hay không. Vì 'Hello'.upper() trả về 'HELLO', nên 'Hello'.upper().isupper() sẽ trả về True.

# c. 'Hello'.upper().lower()
# Phương thức .lower() chuyển đổi tất cả các ký tự trong chuỗi thành chữ thường. Vì 'Hello'.upper() trả về 'HELLO', nên 'Hello'.upper().lower() sẽ trả về 'hello'.