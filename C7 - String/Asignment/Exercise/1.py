# a. 'Hello world!'[1]: Truy cập vào phần tử thứ 2 (bắt đầu từ 0) của chuỗi, kết quả là 'e'.

# b. 'Hello world!'[0:5]: Lấy một đoạn con từ phần tử thứ 1 đến phần tử thứ 5 (không bao gồm) của chuỗi, kết quả là 'Hello'.

# c. 'Hello world!'[:5]: sẽ trả về chuỗi 5 ký tự đầu tiên của chuỗi 'Hello world!', tức là 'Hello'

# d. 'Hello world!'[3:]: Lấy một đoạn con từ phần tử thứ 4 của chuỗi đến hết chuỗi, kết quả là 'lo world!